alert('Введите 2 числа');
var a = prompt('Число 1');
a1 = parseInt(a) % 2;
var b = prompt('Число 2');
b1 = parseInt(b) % 2;
if (a1 === 0 && b1 === 0) {
    alert('Введенное значение a = ' + a + ', b = ' + b + '. Числа четные');
} else {
    alert('Введенное значение a = ' + a + ', b = ' + b + '. Числа нечетные');
}
